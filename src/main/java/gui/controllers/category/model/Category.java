package gui.controllers.category.model;

import api.dto.DtoCategory;
import javafx.beans.property.IntegerProperty;
import javafx.beans.property.SimpleIntegerProperty;
import javafx.beans.property.SimpleStringProperty;
import javafx.beans.property.StringProperty;

public class Category {

    private IntegerProperty id;
    private StringProperty name;
    private StringProperty description;

    public Category(DtoCategory category) {
        this.id = new SimpleIntegerProperty(category.getId());
        this.name = new SimpleStringProperty(category.getName());
        this.description = new SimpleStringProperty(category.getDescription());
    }

    public int getId() {
        return id.get();
    }

    public IntegerProperty idProperty() {
        return id;
    }

    public void setId(int id) {
        this.id.set(id);
    }

    public String getName() {
        return name.get();
    }

    public StringProperty nameProperty() {
        return name;
    }

    public void setName(String name) {
        this.name.set(name);
    }

    public String getDescription() {
        return description.get();
    }

    public StringProperty descriptionProperty() {
        return description;
    }

    public void setDescription(String description) {
        this.description.set(description);
    }

}
